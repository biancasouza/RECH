    private void jLabel19MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel19MouseClicked
        Aluno a = new Aluno();
        AlunoDAO dao = new AlunoDAO();
        
      
        a.setMatricula(Integer.parseInt(mat.getText()));
        a.setCurso(curso.getText());
        a.setSemestre(Integer.parseInt((String) semestre.getSelectedItem()));
        a.setTurno((String) turno.getSelectedItem());
        a.setCpf(p.getCpf());
        
       
        dao.create(a);
       
        mat.setText("");
        curso.setText("");
        semestre.setSelectedItem("");
        turno.setSelectedItem("");
        
    }//GEN-LAST:event_jLabel19MouseClicked

