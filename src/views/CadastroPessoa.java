    public Date converteData (String data) throws ParseException{
        SimpleDateFormat formateData = new SimpleDateFormat("dd/MM/yyyy");
        java.util.Date date;
        date = formateData.parse(data);
        java.sql.Date sqlDate = new java.sql.Date(date.getTime());
        return  sqlDate;
    }
    public  void vincular (Pessoa p, String vinculo){
        if(null == vinculo){
            JOptionPane.showMessageDialog(null, "Selecione o v√≠nculo!");
        }
        else switch (vinculo) {
            case "Aluno (a)":
                {
                    CadastroAluno2 c = new CadastroAluno2 (p);
                    c.setVisible(true);
                    this.dispose();
                    break;
                }
            case "Usuario":
                {
                    int tipo = 1;
                    CadastroFunc c = new CadastroFunc(p, tipo);
                    c.setVisible(true);
                    this.dispose();
                    break;
                }
            case "Funcion√°rio (a)":
                {
                    int tipo =2;
                    CadastroFunc c = new CadastroFunc(p,tipo);
                    c.setVisible(true);
                    this.dispose();
                    break;
                }
            case "Comunidade":
                {
                    System.out.println(p.getCpf());
                    CadsatrarComunidade(p);
                    nome.setText("");
                    end.setText("");
                    tel.setText("");
                    email.setText("");
                    rg.setText("");
                    cpf.setText("");
                    data.setText("");
                    
                    //this.dispose();
                    break;
                }
         
        }
    }
